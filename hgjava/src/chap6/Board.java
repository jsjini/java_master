package chap6;

public class Board {
	String boardNo;
	String boardName;
	String person;
	String content;
	String date;
	
	Board(String boardNo, String boardName, String person, String content, String date){
		this.boardNo = boardNo;
		this.boardName = boardName;
		this.person = person;
		this.content = content;
		this.date = date;
	}
	
	
}//end of class
